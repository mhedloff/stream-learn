import matplotlib.pyplot as plt
import numpy as np

from sklearn.metrics import accuracy_score
from sklearn.naive_bayes import GaussianNB

from strlearn.ensembles import UOB, OnlineBoosting, ONSBoost
from strlearn.evaluators import TestThenTrain
from strlearn.metrics import balanced_accuracy_score
from strlearn.streams import StreamGenerator

N_CHUNKS = 50

stream = StreamGenerator(weights=(2, 5, 0.9), n_chunks=N_CHUNKS, n_features=5, chunk_size=100)

online_boosting = OnlineBoosting(base_estimator=GaussianNB(), n_estimators=5)
onsboost_replacement = ONSBoost(base_estimator=GaussianNB(), n_estimators=5, with_replacement=True)
onsboost = ONSBoost(base_estimator=GaussianNB(), n_estimators=5, with_replacement=False)
uob = UOB(base_estimator=GaussianNB(), n_estimators=5)
clfs = (
    uob,
    onsboost,
    onsboost_replacement,
)
evaluator = TestThenTrain(metrics=(accuracy_score, balanced_accuracy_score), verbose=True)

evaluator.process(stream, clfs)

x = np.linspace(0, N_CHUNKS - 1, N_CHUNKS - 1)
plt.figure(figsize=(18, 9))
plt.plot(x, evaluator.scores[0, :, 1], label='uob')
plt.plot(x, evaluator.scores[1, :, 1], label='onsboost')
plt.plot(x, evaluator.scores[2, :, 1], label='ons_replacement')
